package primitive_types;

public class Short {

    static short defaultShort;

    public static void main(String[] args) {

        short s1 = -12_345;
        System.out.println("s1 " + s1);

        short s2 = 12_344;

        System.out.println("++s2 " + ++s2);

        System.out.println("default " + defaultShort);


    }
}
